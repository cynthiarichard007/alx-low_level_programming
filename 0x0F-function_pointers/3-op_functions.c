#include "3-calc.h"
#include <stdio.h>
#include <stdlib.h>
/**
 * op_add - add two integer
 * @a: first integer
 * @b: second integer
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substract two integer
 * @a: first integer
 * @b: second integer
 * Return: substraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply two integer
 * @a: first integer
 * @b: second integer
 * Return: multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide two integer
 * @a: first integer
 * @b: second integer
 * Return: division
 */
int op_div(int a, int b)
{
		return (a / b);
}
/**
 * op_mod - modulo of two integer
 * @a: first integer
 * @b: second integer
 * Return: modulo
 */
int op_mod(int a, int b)
{
	return (a % b);
}
